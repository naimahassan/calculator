def add(a,b):
	c = a + b
	print(c)

def subtract(a,b):
	c = a - b
	print(c)

def multiply(a,b):
	c = a * b
	print(c)

def divide(a,b):
	c = a / b
	print(c)

def module(a,b):
	c = a % b
	print(c)

def whole_division(a,b):
	c = a // b
	print(c)

def percentage(a,b):
	d = a / b
	c = d /100
	print(c)

def expontiate(a,b):
	c = a ** b 
	print(c)
